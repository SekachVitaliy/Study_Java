public class Main {
    public static int summ (int a,int b) {
        return a+b;
    }
    public static int summ (int a,int b,int c) {
        return a+b+c;
    }
    public static float summ (float a,float b) {
        return a+b;
    }
    public static void main(String[] args) {
        System.out.print(summ(45,45,23));

    }
}